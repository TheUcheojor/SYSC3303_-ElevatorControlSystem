/**
 * This class provides a collection of elevator auto fixing states
 *
 * @author paulokenne
 *
 */
public enum ElevatorAutoFixing {

	/**
	 * Elevator auto fixing enabled
	 */
	AUTO_FIXING_SUCCESS,

	/**
	 * Elevator auto fixing fails when encountered with a fault.
	 */
	AUTO_FIXING_FAILURE
}
